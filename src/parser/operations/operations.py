class Operators():
    @staticmethod
    def add(n, x):
        return float(n) + float(x)

    @staticmethod
    def mul(n, x):
        return float(n) * float(x)

    @staticmethod
    def div(n, x):
        return float(n) / float(x)

    @staticmethod
    def sub(n, x):
        return float(n) - float(x)